## I am loading the input data ------------------------------------------------

my_input <- readLines(file("stdin"))

s <- unlist(strsplit(my_input[1], split = ""))
t <- unlist(strsplit(my_input[2], split = ""))


## ----------------------------------------------------------------------------

###############################################################################

## helper functions -----------------------------------------------------------

isTheSame <- function(x, y){

    # '''
    # For two character vectors "x" and "y" returns TRUE
    # if and only both vectors are absolutely identical.
    # '''
    
    is_the_same <- TRUE
    i <- 1
    
    while(is_the_same & i <= length(x)){
        
        if(x[i] != y[i]){
            
            is_the_same <- FALSE
            
        }else{
            
            i <- i + 1
            
        }
        
    }
    
    return(is_the_same)
    
}


## ----------------------------------------------------------------------------

###############################################################################

## core computations ----------------------------------------------------------

my_output <- NULL

for(i in 1:(length(s) - length(t) + 1)){
    
    if(isTheSame(s[i:(i + length(t) - 1)], t)){
        
        my_output <- c(my_output, i)
        
    }
    
}


## ----------------------------------------------------------------------------

###############################################################################

## I am giving an output ------------------------------------------------------

cat(sprintf("%s", my_output))


